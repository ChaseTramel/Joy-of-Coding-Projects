for i in "oompah!":
    print(i.upper())
print("\n \n")

list = [0,1,4,16,64,256]
for j in list:
    print(j, end = " ")
print("\n \n")

for k in "scream":
    if k == "m":
        print(k.upper(), end = " for ice cream!")
    else:
        print(k.upper(), end = "-")
print("\n \n")

sum = 0
for l in range(-400, 401, 2):
    sum += l
print(sum)
print("\n \n")

for m in "HALLELUJAH!":
    if m == "!":
        print(m, end = "")
    else:
        print(m, end = "--")
